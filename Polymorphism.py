# This demonstrates polymorphism using the + operator.
# The + operator performs different operations based on data types.
# It adds integers, concatenates strings, and merges lists.
# Polymorphism allows the same operator to work in multiple forms.


print(20+30) #addition
print("Shital"+ ""+"Bhokare") #concatenation
print([1,2,3]+[6,7,8]) #merge